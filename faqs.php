            <p style="text-align: left;"><strong><span style="color: #cc99ff;">Is Into Modeling an agency?</span></strong></p>
<p class="font14" style="text-align: left;">No, Into Modeling is not an 
agency and Into Modeling does not find models work. We help future 
models find the way to break into the modeling industry and guide them 
to be able to approach the right agencies with the right information.</p>
<p style="text-align: left;"><strong><span style="color: #cc99ff;">Is having a portfolio necessary?</span></strong></p>
<p class="font14" style="text-align: left;">Yes, a portfolio is the key 
to your success. It is your CV. This is how you show your potential and 
flexibility as a model to all clients. Your clients will hire you 
because of your modeling portfolio so, the better your portfolio, the 
higher chance of signing with an agency and getting work.</p>
<p style="text-align: left;"><strong><span style="color: #cc99ff;">Does building a portfolio have a price?</span></strong></p>
<p class="font14" style="text-align: left;">Every model will need to pay
 for their portfolio. In general, top fashion agencies will invest and 
build portfolios for their models and will deduct a large number from 
their first pay. But, not every model is lucky to sign to a top fashion 
agency as they have very strict conditions.</p>
<p style="text-align: left;"><span style="color: #cc99ff;"><strong>To approach agencies should you use professional images?</strong></span></p>
<p class="font14" style="text-align: left;">Some agencies ask for 
natural, relaxed images. However, in general, and even these agencies, 
will also need professional images to market yourself as a model.</p>
<p style="text-align: left;"><strong><span style="color: #cc99ff;">Does Into Modeling work with studios?</span></strong></p>
<p class="font14" style="text-align: left;">The Into Modeling is owned 
by Kube Studios, which owns studios in Miami, London, Birmingham, and 
Manchester. While both serve different purposes they work closely 
together. We would encourage you to compare our work to the work of 
others.</p>
<p style="text-align: left;"><strong><span style="color: #cc99ff;">Is work guaranteed by signing with a model agency?</span></strong></p>
<p class="font14" style="text-align: left;">Even though the agency 
represents and markets you, the client is the one who ultimately decides
 which model they want to work with and they do that by looking at 
portfolios.&nbsp;So, in reality, no Agency will guarantee work.</p>
<p style="text-align: left;"><span style="color: #cc99ff;"><strong>After how long can you get a job?</strong></span></p>
<p class="font14" style="text-align: left;">There are many cases where 
models get jobs from the moment they sign up to3 months after they sign 
up. This depends on what the clients are looking for and also on trends,
 fashions and clients.</p>
<p style="text-align: left;"><span style="color: #cc99ff;"><strong>How much can I get paid?</strong></span></p>
<p class="font14" style="text-align: left;">Once you are signed with an 
agency you need to understand very well the percentage your agent will 
take from your earning. Always refer back to your contract to keep track
 of your earnings.&nbsp;Rates for models depend on the client, some 
rates can be hourly and some, daily.</p>
<p style="text-align: left;"><strong><span style="color: #cc99ff;">Will traveling be part of the job?</span></strong></p>
<p class="font14" style="text-align: left;">Modeling can involve 
traveling and this depends on the location of shoots and castings. Your 
agent is the one who will most likely arrange your trip.</p>
<p style="text-align: left;"><strong><span style="color: #cc99ff;">Can working as a model affect my studies?</span></strong></p>
<p class="font14" style="text-align: left;">If you are in full time 
education your agency should be aware and will probably only call you at
 the weekends and holidays so your studies are not interrupted.&nbsp;The
 agency’s job is to represent you and find work; it is up to you to 
decide which castings and shoots you would like to go to. You need to be
 able to say no if it is not convenient for you.</p>
<p style="text-align: left;"><strong><span style="color: #cc99ff;">Can I sign up with more than one agency?</span></strong></p>
<p class="font14" style="text-align: left;">Most models within the 
industry do not get exclusivity contracts – they work on a freelance 
basis. This can be convenient for them because it means they can work 
with more than one agency.</p>
<p style="text-align: left;"><strong><span style="color: #cc99ff;">What shape or size do I have to be?</span></strong></p>
<p class="font14" style="text-align: left;">What is important, is to be 
realistic and approach the right agencies with the precise style of 
portfolio. The model industry has been updated in the last five years 
and to be successful, you no longer have to be a size zero. Nowadays, 
many of the world’s top designers have banned size zero models from 
appearing in catwalk shows.</p>
<p style="text-align: left;"><strong><span style="color: #cc99ff;">Can a model have tattoos?</span></strong></p>
<p class="font14" style="text-align: left;">With a third of young people
 in Into Modeling now having a tattoo, Nowadays, advertisers realize 
that their target market have some form of body ink and want to view 
models with tattoos. Tattoos were not accepted in the industry a while 
back, but this has changed, depending on their size and location.</p>

