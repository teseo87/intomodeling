Modeling have some useful tips and advice for parents.</p>
<p>Because the safety of your child is our number one priority, we have a
 strict rule that all under 22-year-old models must be accompanied by a 
parent for any of the services we provide. We believe that below 22, 
your child will benefit from the support you can provide, and make 
objective decisions regardless their aspiration.</p>
<p>Below are the positive and negative aspects of your child trying to break into the industry:</p>
<h4>The Benefits</h4>
<ul>
<li>Becoming a model may provide a fairly lucrative income for what is usually part time work</li>
<li>They may use modeling to support themselves through higher education</li>
<li>It helps the child develop: organizational skills, experience, 
dealing with commercial issues and decisions, meeting new and varied 
people, traveling to new places</li>
<li>It is amazing for a child’s confidence and their development</li>
<li>Brings parent and child together in a common goal</li>
<li>It promotes parent and child bonding to pursue a common goal</li>
<li>Encourages children and young adults to enter new situations without fear</li>
<li>Any child under the age of 16 or in full time education must be 
licensed to work in modeling or entertainment. Respectable agencies 
which represent children are aware of and adhere to Child Licensing Laws
 and will organize this for you.</li>
</ul>
<h4>The Drawbacks</h4>
<ul>
<li>It is very competitive, and rejection is part of the process</li>
<li>It might involve a travelling</li>
<li>At first, there are barriers to overcome</li>
<li>There are costs associated with becoming a model, although these are tax deductible</li>
</ul>
<p>Modeling can be an exciting and enriching opportunity for your child.
 If your child is to succeed as a model, they will need your full 
support. You will need to escort them to their modeling shoots and 
ensure that the jobs they take are appropriate for a child of their age,
 since you will probably be their manager. All models work as part of a 
team – as your child’s manager, you will become part of that team.</p>

