                                <h3>Becoming a Body Parts Model</h3>
<p class="font14"><strong>Although modeling parts of your body that have
 a special quality may sound peculiar, being a body parts model can be 
quite profitable.</strong></p>
<p>Som vetimes, commercial or fashion models do not have nice 
enough&nbsp;feet to have a close up of their shoes even if they look 
good in a full length picture modelling an outfit. This is the kind of 
situation where the body parts model is hired. They are employed because
 they usually specialize in modeling a specific part of their 
body.&nbsp;This specialized type of model can be used in photographic 
shoots to help promote and sell products. They may also be summoned to 
participate in films where the producers or directors are looking for 
particularly beautiful hands or amazing legs.</p>
<p>Some models only model parts. Even so, commercial, fashion, petite, 
mature, plus size and glamour models can also do parts modeling as long 
as they meet the required standards. To model products like: rings and 
bracelets for jewelers, nail polish, nail extensions and hand creams, 
hand models are used.</p>
<h4>Requirements for Hand Models include:</h4>
<ul>
<li>Smooth skin</li>
<li>Long, slender graceful looking hands and fingers</li>
<li>Great shape and the ability to pose your hands in a relaxed manner</li>
<li>No&nbsp;blemishes or uneven skin tone</li>
<li>Men’s hands shouldn’t be too hairy</li>
<li>Neat and even nails</li>
</ul>
<h4>Requirements for Foot Models include:</h4>
<ul>
<li>Smooth skin</li>
<li>Evenly shaped toes</li>
<li>No corns or hang nails</li>
<li>No blemishes or uneven skin tone</li>
<li>Neat and even nails</li>
<li>Attractive ankles</li>
</ul>
<p>Foot models&nbsp;may advertise items like: <em>shoes, ankle bracelets, toe rings, creams, sprays, polishes or socks.&nbsp;</em><br>
Leg models sell lots of products such as <em>stockings, razors, bath products and lotions</em>.
 They are normally associated with tall models and their legs should 
look long, shapely, smooth, be well moisturized, waxed, free of 
blemishes and varicose veins.</p>
<p>Even though these are the main three types of body-parts modelling, 
there is also a market for ears, hair, bottoms, necks, eyes and lips.</p>


